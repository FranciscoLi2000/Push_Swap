#include "libft.h"

/*
** Outputs number to file descriptor
** Writes integer n to given file descriptor
** Handles negative numbers and INT_MIN
** No return value
*/

void	ft_putnbr_fd(int n, int fd)
{
	long	num;

	num = n;
	if (num < 0)
	{
		ft_putchar_fd('-', fd);
		num = -num;
	}
	if (num >= 10)
		ft_putnbr_fd(num / 10, fd);
	ft_putchar_fd((num % 10) + '0', fd);
}
