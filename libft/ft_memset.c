#include "libft.h"

/*
** Fills memory with constant byte
** Writes byte c to first len bytes of memory area b
** Returns original pointer b
*/

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*ptr;
	size_t			i;

	ptr = (unsigned char *)b;
	i = 0;
	while (i < len)
	{
		ptr[i] = (unsigned char)c;
		i++;
	}
	return (b);
}
