#include "libft.h"

/*
** Counts digits in number
** Helper for ft_itoa - calculates string length needed
** Returns number of digits including sign
*/

static int	ft_numlen(int n)
{
	int	len;

	len = 0;
	if (n <= 0)
		len = 1;
	while (n != 0)
	{
		n /= 10;
		len++;
	}
	return (len);
}

/*
** Converts integer to string
** Creates string representation of integer n
** Handles negative numbers and INT_MIN
** Returns newly allocated string, NULL on malloc failure
** Caller must free returned string
*/

char	*ft_itoa(int n)
{
	char	*str;
	int		len;
	long	num;

	num = n;
	len = ft_numlen(n);
	str = (char *)malloc(sizeof(char) * (len + 1));
	if (!str)
		return (NULL);
	str[len] = '\0';
	if (num < 0)
	{
		str[0] = '-';
		num = -num;
	}
	if (num == 0)
		str[0] = '0';
	while (num > 0)
	{
		str[--len] = (num % 10) + '0';
		num /= 10;
	}
	return (str);
}
